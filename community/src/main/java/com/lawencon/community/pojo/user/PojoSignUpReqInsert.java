package com.lawencon.community.pojo.user;

public class PojoSignUpReqInsert {

	private String email;
	private String password;
	private String codeVerfication;
	private String fullName;
	private String phoneNumber;
	private String company;
	private String industryId;
	private String positionId;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getCodeVerfication() {
		return codeVerfication;
	}
	public void setCodeVerfication(String codeVerfication) {
		this.codeVerfication = codeVerfication;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getIndustryId() {
		return industryId;
	}
	public void setIndustryId(String industryId) {
		this.industryId = industryId;
	}
	public String getPositionId() {
		return positionId;
	}
	public void setPositionId(String positionId) {
		this.positionId = positionId;
	}
	
	
	
	
}
