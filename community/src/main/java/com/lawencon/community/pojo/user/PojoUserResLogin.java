package com.lawencon.community.pojo.user;

public class PojoUserResLogin {
	private String roleCode;
	private String token;
	private String fullName;
	private String imgProfileId;
	
	public String getRoleCode() {
		return roleCode;
	}
	public void setRoleCode(String roleCode) {
		this.roleCode = roleCode;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getImgProfileId() {
		return imgProfileId;
	}
	public void setImgProfileId(String imgProfileId) {
		this.imgProfileId = imgProfileId;
	}
	
}
